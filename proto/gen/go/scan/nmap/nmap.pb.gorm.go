// ToORM runs the BeforeToORM hook if present, converts the fields of this
// object to ORM format, runs the AfterToORM hook, then returns the ORM object
func (m *Run) ToORM(ctx context.Context) (RunORM, error) {
	to := RunORM{}
	var err error
	if prehook, ok := interface{}(m).(RunWithBeforeToORM); ok {
		if err = prehook.BeforeToORM(ctx, &to); err != nil {
			return to, err
		}
	}
	if m.Id != nil {
		to.Id, err = go_uuid.FromString(m.Id.Value)
		if err != nil {
			return to, err
		}
	} else {
		to.Id = go_uuid.Nil
	}
	if m.CreatedAt != nil {
		t := m.CreatedAt.AsTime()
		to.CreatedAt = &t
	}
	if m.UpdatedAt != nil {
		t := m.UpdatedAt.AsTime()
		to.UpdatedAt = &t
	}
	to.Args = m.Args
	to.ProfileName = m.ProfileName
	to.Scanner = m.Scanner
	to.StartStr = m.StartStr
	to.Version = m.Version
	to.XMLOutputVersion = m.XMLOutputVersion
	if m.Debugging != nil {
		tempDebugging, err := m.Debugging.ToORM(ctx)
		if err != nil {
			return to, err
		}
		to.Debugging = &tempDebugging
	}
	if m.Stats != nil {
		tempStats, err := m.Stats.ToORM(ctx)
		if err != nil {
			return to, err
		}
		to.Stats = &tempStats
	}
	if m.ScanInfo != nil {
		tempScanInfo, err := m.ScanInfo.ToORM(ctx)
		if err != nil {
			return to, err
		}
		to.ScanInfo = &tempScanInfo
	}
	if m.Start != nil {
		t := m.Start.AsTime()
		to.Start = &t
	}
	if m.Verbose != nil {
		tempVerbose, err := m.Verbose.ToORM(ctx)
		if err != nil {
			return to, err
		}
		to.Verbose = &tempVerbose
	}
	for _, v := range m.Hosts {
		if v != nil {
			if tempHosts, cErr := v.ToORM(ctx); cErr == nil {
				to.Hosts = append(to.Hosts, &tempHosts)
			} else {
				return to, cErr
			}
		} else {
			to.Hosts = append(to.Hosts, nil)
		}
	}
	for _, v := range m.PreScripts {
		if v != nil {
			if tempPreScripts, cErr := v.ToORM(ctx); cErr == nil {
				to.PreScripts = append(to.PreScripts, &tempPreScripts)
			} else {
				return to, cErr
			}
		} else {
			to.PreScripts = append(to.PreScripts, nil)
		}
	}
	for _, v := range m.PostScripts {
		if v != nil {
			if tempPostScripts, cErr := v.ToORM(ctx); cErr == nil {
				to.PostScripts = append(to.PostScripts, &tempPostScripts)
			} else {
				return to, cErr
			}
		} else {
			to.PostScripts = append(to.PostScripts, nil)
		}
	}
	for _, v := range m.Targets {
		if v != nil {
			if tempTargets, cErr := v.ToORM(ctx); cErr == nil {
				to.Targets = append(to.Targets, &tempTargets)
			} else {
				return to, cErr
			}
		} else {
			to.Targets = append(to.Targets, nil)
		}
	}
	for _, v := range m.TaskBegin {
		if v != nil {
			if tempTaskBegin, cErr := v.ToORM(ctx); cErr == nil {
				to.TaskBegin = append(to.TaskBegin, &tempTaskBegin)
			} else {
				return to, cErr
			}
		} else {
			to.TaskBegin = append(to.TaskBegin, nil)
		}
	}
	for _, v := range m.TaskProgress {
		if v != nil {
			if tempTaskProgress, cErr := v.ToORM(ctx); cErr == nil {
				to.TaskProgress = append(to.TaskProgress, &tempTaskProgress)
			} else {
				return to, cErr
			}
		} else {
			to.TaskProgress = append(to.TaskProgress, nil)
		}
	}
	for _, v := range m.TaskEnd {
		if v != nil {
			if tempTaskEnd, cErr := v.ToORM(ctx); cErr == nil {
				to.TaskEnd = append(to.TaskEnd, &tempTaskEnd)
			} else {
				return to, cErr
			}
		} else {
			to.TaskEnd = append(to.TaskEnd, nil)
		}
	}
	// Repeated type string is not an ORMable message type
	if posthook, ok := interface{}(m).(RunWithAfterToORM); ok {
		err = posthook.AfterToORM(ctx, &to)
	}
	return to, err
}

