type PrivateORM struct {
	CreatedAt *time.Time
	Data      string       `gorm:"not null"`
	Id        go_uuid.UUID `gorm:"type:uuid;primary_key"`
	JTRFormat string
	Type      int32 `gorm:"not null"`
	UpdatedAt *time.Time
}

// TableName overrides the default tablename generated by GORM
func (PrivateORM) TableName() string {
	return "privates"
}

// ToORM runs the BeforeToORM hook if present, converts the fields of this
// object to ORM format, runs the AfterToORM hook, then returns the ORM object
func (m *Private) ToORM(ctx context.Context) (PrivateORM, error) {
	to := PrivateORM{}
	var err error
	if prehook, ok := interface{}(m).(PrivateWithBeforeToORM); ok {
		if err = prehook.BeforeToORM(ctx, &to); err != nil {
			return to, err
		}
	}
	if m.Id != nil {
		to.Id, err = go_uuid.FromString(m.Id.Value)
		if err != nil {
			return to, err
		}
	} else {
		to.Id = go_uuid.Nil
	}
	if m.CreatedAt != nil {
		t := m.CreatedAt.AsTime()
		to.CreatedAt = &t
	}
	if m.UpdatedAt != nil {
		t := m.UpdatedAt.AsTime()
		to.UpdatedAt = &t
	}
	to.Type = int32(m.Type)
	to.Data = m.Data
	to.JTRFormat = m.JTRFormat
	if posthook, ok := interface{}(m).(PrivateWithAfterToORM); ok {
		err = posthook.AfterToORM(ctx, &to)
	}
	return to, err
}

// ToPB runs the BeforeToPB hook if present, converts the fields of this
// object to PB format, runs the AfterToPB hook, then returns the PB object
func (m *PrivateORM) ToPB(ctx context.Context) (Private, error) {
	to := Private{}
	var err error
	if prehook, ok := interface{}(m).(PrivateWithBeforeToPB); ok {
		if err = prehook.BeforeToPB(ctx, &to); err != nil {
			return to, err
		}
	}
	to.Id = &types.UUID{Value: m.Id.String()}
	if m.CreatedAt != nil {
		to.CreatedAt = timestamppb.New(*m.CreatedAt)
	}
	if m.UpdatedAt != nil {
		to.UpdatedAt = timestamppb.New(*m.UpdatedAt)
	}
	to.Type = PrivateType(m.Type)
	to.Data = m.Data
	to.JTRFormat = m.JTRFormat
	if posthook, ok := interface{}(m).(PrivateWithAfterToPB); ok {
		err = posthook.AfterToPB(ctx, &to)
	}
	return to, err
}

// The following are interfaces you can implement for special behavior during ORM/PB conversions
// of type Private the arg will be the target, the caller the one being converted from

